-- CREATE TABLE
CREATE TABLE USERS 
(
	ID CHAR(100) PRIMARY KEY,
	AVARTAR IMAGE,
	USERNAME NVARCHAR(100),
	EMAIL NVARCHAR(100),
	PHONE CHAR(10),
	USER_PASSWORD NVARCHAR(100),
	ACCESS CHAR(100),
)

CREATE TABLE CART 
(
	ID_USER CHAR(100),
	ID_PRODUCT CHAR(100),
	QUANTITY INT,
	PRIMARY KEY(ID_USER, ID_PRODUCT),
	CHECK (QUANTITY >= 0)
)

CREATE TABLE ACCESS_LEVELS 
(
	ID CHAR(100) PRIMARY KEY,
	LEVEL_NAME NVARCHAR(100)
)

CREATE TABLE VERTIFI_EMAIL 
(
	ID CHAR(100) PRIMARY KEY,
	CODE CHAR(10),
	EXPIRED_TIME TIME
)

CREATE TABLE PRODUCTS 
(
	ID CHAR(100) PRIMARY KEY,
	ID_BRAND CHAR(100),
	NAME_PD NVARCHAR(200),
	METERIAL NVARCHAR(200),
	DIMENSION NVARCHAR(200),
	IMAGE_MAIN IMAGE,
	KIND NVARCHAR(200),
	TRADEMARK NVARCHAR(200),
	DECRIPTION NVARCHAR(500),
	AMOUNT INT,
	COLOR NVARCHAR(200),
	PRICE INT,
	DATE_ADDED DATE,
	CHECK (DIMENSION LIKE '%x%x%'),
	CHECK (AMOUNT >= 0)
)

CREATE TABLE BRANDS 
(
	ID CHAR(100) PRIMARY KEY,
	NAME_BRAND VARCHAR(200),
	ORIGIN VARCHAR(200)
)

CREATE TABLE ORDERS
(
	ID CHAR(100) PRIMARY KEY,
	ID_USER CHAR(100),
	DATE_ORDER DATE,
	DATE_RECEIPT DATE,
	STATUS_ORDER VARCHAR(200),
	CHECK (STATUS_ORDER IN ('Đang giao', 'Bị hủy', 'Hoàn thành'))
)

CREATE TABLE DETAIL_ORDER 
(
	ID CHAR(100),
	ID_PRODUCT CHAR(100),
	AMOUNT INT,
	PRICE FLOAT,
	PRIMARY KEY (ID, ID_PRODUCT),
	CHECK (AMOUNT >= 0),
	CHECK (PRICE >= 0)
)

CREATE TABLE WISH_LIST 
(
	ID_USER CHAR(100),
	ID_PRODUCT CHAR(100),
	PRIMARY KEY (ID_USER, ID_PRODUCT)
)

CREATE TABLE VOUCHERS
(
	ID CHAR(100) PRIMARY KEY,
	PERCENT_DECREASE FLOAT,
	DATE_START SMALLDATETIME,
	DATE_END SMALLDATETIME
)

CREATE TABLE APPLY_VOUCHER
(
	ID CHAR(100),
	ID_PRODUCT CHAR(100),
	CONDITION CHAR(100)
	PRIMARY KEY (ID, ID_PRODUCT)
)

CREATE TABLE CONDITIONS
(
	ID CHAR(100) PRIMARY KEY,
	DETAIL NVARCHAR(500)
)

CREATE TABLE MANAGEMENT_VOUCHER
(
	ID_USER CHAR(100),
	ID_VOUCHER CHAR(100),
	STATUS_VOUCHER CHAR(100),
	PRIMARY KEY (ID_USER, ID_VOUCHER)
)
GO


